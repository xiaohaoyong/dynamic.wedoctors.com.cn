    public static function getAreaNumber($str='',$ext = false,$province = 0)
    {
        $str = trim($str);

        switch ($str)
        {
            case '卢湾' :
                $str = '黄浦';
                break;
            case '襄樊' :
                $str = '襄阳';
                break;
            case '怒江傈' :
                $str = '怒江';
                break;
            case '黔南' :
                $str = '黔南布';
                break;
            case '南汇' :
                $str = '浦东新区';
                break;
        }

        $_AREA_LIST = self::data();
        if($ext)
        {
            if(!empty($province))
            {
                foreach ($_AREA_LIST as $key => $val)
                {
                    $listKey = substr($key,0,2);
                    if($listKey == $province)
                    {
                        $list[$key] = $val;
                    }
                }
                $_AREA_LIST = $list;
            }

            $result = array_search($str, $_AREA_LIST);
            if (empty($result))
            {
                $result = empty($province) ? array_search($str.'省', $_AREA_LIST) : '';
                if(empty($result))
                {
                    $result = array_search($str.'市', $_AREA_LIST);
                    if(empty($result))
                    {
                        $result = array_search($str.'区', $_AREA_LIST);
                        if(empty($result))
                        {
                            $result = array_search($str.'县', $_AREA_LIST);
                            if(empty($result))
                            {
                                $result = array_search($str.'盟', $_AREA_LIST);
                                if(empty($result))
                                {
                                    $result = array_search($str.'州', $_AREA_LIST);
                                }
                            }
                        }
                    }
                }
            }
            $length = strlen($result);
            if($length == 6)
            {
                $result = AreaSelect::getParentId($result);
            }
            return $result;
        }

        return array_search($str, $_AREA_LIST);
    }
    
    public static function getAreaString($key)
    {
        $_AREA_LIST = self::data();
        return empty($_AREA_LIST[$key]) ? '' : $_AREA_LIST[$key];
    }
